            <h3 class="text-center">Dia da Internet Segura: como se proteger na web</h3>
            <br>
            <p>Entenda quais perigos nos cercam na internet e como se proteger. Anualmente a comunidade internacional escolhe uma data de Fevereiro para ser o Dia da Internet Segura. Em 2022, o dia 8 foi o escolhido para conscientizar usuários de todo o mundo sobre os riscos virtuais que corremos todos os dias, como o roubo de dados.
            <br>
            Preocupada com a segurança digital de toda a comunidade, a Telecontrol destacou algumas recomendações sobre como se proteger on-line. Confira:
            <br>
            <br>
               <div class="titulo text-center">Prefira não salvar suas senhas no navegador</div> 
            <br>
            <br>
            Esse hábito que pode facilitar no dia a dia também viabiliza golpes digitais. Ao manter suas informações de acesso salvas no navegador, o usuário fica exposto a diferentes ameaças. Uma excelente alternativa é optar por programas que armazenam senhas, de preferência sem conexão com a internet.
            <br>
            <br>
               <div class="titulo text-center">Ao acessar redes públicas, utilize VPN</div> 
            <br>
            <br>
            Quando utilizar redes públicas de wi-fi, realize esta conexão a partir de uma Rede Virtual Privada (VPN, nas sigla em inglês). Isso porque o VPN impede que usuários mal-intencionados usem a rede para acessar dados do computador.
            <br>
            <br>
                <div class="titulo text-center">Verifique sempre a segurança do site</div>
            <br>
            <br>
            Ao acessar um novo endereço na web, confira a confiabilidade daquele link. Diversas plataformas disponíveis on-line identificam e sinalizam sites que podem ser nocivos aos usuários e seus dados.
            <br>
            <br>
               <div class="titulo text-center">Tenha cuidado com links encurtados</div> 
            <br>
            <br>
            Sempre verifique os links que for acessar, especialmente os que estiverem em sua forma encurtada (como os bit.ly). Esse tipo de endereço é comum nas redes sociais, mas pode servir de acesso a hackers e criminosos digitais.
            <br>
            <br>
                <div class="titulo text-center">Cheque o cadeado do navegador</div>
            <br>
            <br>
            Os cadeados que acompanham endereços digitais mostram sua segurança. Portais que não contam com o cadeado são mais vulneráveis ao ataque de hackers e criminosos, que têm acesso a informações e dados pessoais dos usuários, como dados de pagamento.
            <br>
            <br>
               <div class="titulo text-center">Conte apenas com ferramentas reconhecidas pelo mercado</div> 
            <br>
            <br>
            Ao selecionar suas plataformas e ferramentas digitais, prefira sempre as reconhecidas pelo mercado, especialmente de marcas que demonstram preocupação com segurança digital. Tenha segurança e tranquilidade ao armazenar seus dados pessoais e profissionais.</p>
