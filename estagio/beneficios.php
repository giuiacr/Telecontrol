            <br>
            -Diminuição o desperdício;
            <br>
            -Criação de cooperativas fortes;
            <br>
            -Geração de emprego e renda pela comercialização dos recicláveis;
            <br>
            -Melhorias e diminuição de gastos na limpeza pública.</p>
